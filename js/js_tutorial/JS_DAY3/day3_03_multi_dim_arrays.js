// how to create a multidimensional array

let matrix = [[1, 2, 3], [4, 5, 6]];
console.log(matrix);

for(let row of matrix) {
    console.log(row);
    for(let cell of row) {
        console.log(cell);
    }
    //console.log('\n')
}


